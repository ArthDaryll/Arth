    private void populateInvoiceData(String invoiceNumber) {
        try {
            // Establish database connection
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/arth", "arth", "arth123");
            
            // Prepare SQL statement
            String sql = "SELECT * FROM invoice WHERE id_no = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, invoiceNumber);
            
            // Execute query
            ResultSet rs = pstmt.executeQuery();
            
            // Check if result set has data
            if (rs.next()) {
                // Populate JLabels
                nameT.setText(rs.getString("name"));
                addressT.setText(rs.getString("address"));
                emailT.setText(rs.getString("email"));
                contactT.setText(rs.getString("contact"));
                dateissuedT.setText(rs.getString("date_issued"));
                duedateT.setText(rs.getString("due_date"));
                surchargesT.setText(rs.getString("surcharge"));
                discountT.setText(rs.getString("discount"));
                meterreadingT.setText(rs.getString("meter_reading"));
                tariffrateT.setText(rs.getString("tariff_rate"));
                taxT.setText(rs.getString("tax"));
                totalT.setText(rs.getString("total_amount_due")); // Set total directly from database
            } else {
                // No data found for the given invoice number
                JOptionPane.showMessageDialog(this, "Invoice not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            // Close resources
            rs.close();
            pstmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
